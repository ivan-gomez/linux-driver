#include "operations.h"

long add(int numberA, int numberB)
{
	return (long)numberA + (long)numberB;
}

long substract(int numberA, int numberB)
{
	return numberA - numberB;
}

long multiply(int numberA, int numberB)
{
	return numberA * numberB;
}

long divide(int numberA, int numberB)
{
	return numberA / numberB;
}
